class Pixel{
	private int red;
	private int green;
	private int blue;

	public Pixel(){
		this.red = 0;
		this.green = 0;
		this.blue = 0;
	}

	public int getRed(){
		return this.red;
	}
	public int getGreen(){
		return this.green;
	}
	public int getBlue(){
		return this.blue;
	}

	public void setRed(int red){
		this.red = red;
	}
	public void setGreen(int green){
		this.green = green;
	}
	public void setBlue(int blue){
		this.blue = blue;
	}
}